//assign.cpp
// demonstrates arithmetic assignments operators 
#include <iostream>
using namespace std;
int main()
{
    int ans =27;
    ans+=2;
    cout<<ans<<",";
    ans-=2;
    cout<<ans<<",";
    ans*=4;
    cout<<ans<<",";
    ans/=4;
    cout<<ans<<",";
    ans%=3;
    cout<<ans<<",";
    return 0;
}
